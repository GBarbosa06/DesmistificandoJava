public class Aula002 {
    /*
     *      -- TIPOS PRIMITIVOS --
     *   INTEIROS
     * byte - 2 bytes - x = 1
     * short - 4 bytes - x = 15
     * int - 8 bytes - x = 20 **
     * long - 16 bytes - x = 1000 **
     *
     *   PONTOS FLUTUANTES
     * double - 16 bytes - x = 12.2 **
     * float - 8 bytes - x = 12.2f
     *
     *    BOOLEANO
     * boolean - 1 byte - x = true ou x = false **
     *
     *    CARACTER
     * char - 2 bytes - x = 'a' **
     *
     *   String
     * String - x bytes - str = "Frase ou uma palavra ou qualquer coisa" **
     */

    // <tipo> <nome>;

    public static void main(String[] args) {
        int n1 = 12;
        int n2 = 88;
        int n3 = n1 + n2;
        System.out.println("O resultado é: " + n3);

        // nomes claros
        int primeiroNumero = 12;

        // variaveis começam com nome minúsculo
        String texto;

        // camel case
        boolean camelCase = true;

        // inicializar variáveis antes
    }

}
